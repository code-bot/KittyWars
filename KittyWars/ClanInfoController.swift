import UIKit

class ClanInfoController: UIViewController {
    
    @IBOutlet weak var ninjaCounter: UIProgressView!
    @IBOutlet weak var pirateCounter: UIProgressView!
    @IBOutlet weak var ninjaCountLabel: UILabel!
    @IBOutlet weak var pirateCountLabel: UILabel!
    
    override func viewDidLoad() {
        super.viewDidLoad()
        // Do any additional setup after loading the view, typically from a nib.
        ninjaCountLabel.text = ""
        pirateCountLabel.text = ""
        myRootRef.childByAppendingPath("ninjasKillCount").observeEventType(.Value, withBlock: {
            snapshot in
            if let count = snapshot.value as? Int {
                self.ninjaCounter.progress = Float(count)/200.0
                self.ninjaCountLabel.text = String(count) + "/200"
            }
        })
        
        myRootRef.childByAppendingPath("piratesKillCount").observeEventType(.Value, withBlock: {
            snapshot in
            if let count = snapshot.value as? Int {
                self.pirateCounter.progress = Float(count)/200.0
                self.pirateCountLabel.text = String(count) + "/200"
            }
        })
    }
    
    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }

}
